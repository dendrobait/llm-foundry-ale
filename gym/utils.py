# Letras (incluindo acentuação)
_ALPHABETS = r"([A-Za-zÀ-ÖØ-öø-ÿ])"

# Prefixos comuns antes de nomes
_PREFIXES = r"(Sr|Sra|Srta|Dr|Dra|Prof|Profa|Eng|Arq|Pe|Fr)[.]"

# Sufixos nominais/corporativos
_SUFFIXES = r"(Jr|Sr|Neto|Filho|Sobr|ME|LTDA|SA)"

# Palavras que costumam iniciar nova frase
_STARTERS = r"(Ele\s|Ela\s|Elx\s|Isto\s|Isso\s|Aquilo\s|Eles\s|Elas\s|Nós\s|Mas\s|Porém\s|Entretanto\s|Contudo\s|Todavia\s|Além disso\s|No entanto\s|Assim\s|Então\s|Portanto\s)"

# Siglas com pontos (padrão estilo "E.U.A." ou "U.S.")
_ACRONYMS = r"([A-ZÁÉÍÓÚÂÊÔÃÕ][.][A-ZÁÉÍÓÚÂÊÔÃÕ][.](?:[A-ZÁÉÍÓÚÂÊÔÃÕ][.])?)"

# Domínios de sites mais comuns
_WEBSITES = r"[.](com|com\.br|net|org|gov|gob|edu|pt|br|me)"

# Dígitos
_DIGITS = r"([0-9])"

# Múltiplos pontos
_MULTIPLE_DOTS = r"\.{2,}"


def split_into_sentences(text):
    """Split the text into sentences.

    Args:
      text: A string that consists of more than or equal to one sentences.

    Returns:
      A list of strings where each string is a sentence.
    """
    text = " " + text + "  "
    text = text.replace("\n", " ")
    text = re.sub(_PREFIXES, "\\1<prd>", text)
    text = re.sub(_WEBSITES, "<prd>\\1", text)
    text = re.sub(_DIGITS + "[.]" + _DIGITS, "\\1<prd>\\2", text)
    text = re.sub(
        _MULTIPLE_DOTS,
        lambda match: "<prd>" * len(match.group(0)) + "<stop>",
        text,
    )
    if "Ph.D" in text:
        text = text.replace("Ph.D.", "Ph<prd>D<prd>")
    text = re.sub(r"\s" + _ALPHABETS + "[.] ", " \\1<prd> ", text)
    text = re.sub(_ACRONYMS + " " + _STARTERS, "\\1<stop> \\2", text)
    text = re.sub(
        _ALPHABETS + "[.]" + _ALPHABETS + "[.]" + _ALPHABETS + "[.]",
        "\\1<prd>\\2<prd>\\3<prd>",
        text,
    )
    text = re.sub(_ALPHABETS + "[.]" + _ALPHABETS + "[.]", "\\1<prd>\\2<prd>", text)
    text = re.sub(" " + _SUFFIXES + "[.] " + _STARTERS, " \\1<stop> \\2", text)
    text = re.sub(" " + _SUFFIXES + "[.]", " \\1<prd>", text)
    text = re.sub(" " + _ALPHABETS + "[.]", " \\1<prd>", text)
    if "”" in text:
        text = text.replace(".”", "”.")
    if '"' in text:
        text = text.replace('."', '".')
    if "!" in text:
        text = text.replace('!"', '"!')
    if "?" in text:
        text = text.replace('?"', '"?')
    text = text.replace(".", ".<stop>")
    text = text.replace("?", "?<stop>")
    text = text.replace("!", "!<stop>")
    text = text.replace("<prd>", ".")
    sentences = text.split("<stop>")
    sentences = [s.strip() for s in sentences]
    if sentences and not sentences[-1]:
        sentences = sentences[:-1]
    return sentences


def count_words(text):
    """Counts the number of words."""
    tokenizer = nltk.tokenize.RegexpTokenizer(r"\w+")
    tokens = tokenizer.tokenize(text)
    num_words = len(tokens)
    return num_words


@functools.lru_cache(maxsize=None)
def _get_sentence_tokenizer():
    return nltk.data.load("nltk:tokenizers/punkt/portuguese.pickle")


def count_sentences(text):
    """Count the number of sentences."""
    tokenizer = _get_sentence_tokenizer()
    tokenized_sentences = tokenizer.tokenize(text)
    return len(tokenized_sentences)


